
public class Moule
{
        private int valeur;
        private int x;
        private int y;

        public Moule(int x0, int y0, int val0)
        {
          this.x=x0;
          this.y=y0;
          this.valeur=val0;
        }
        public int getX()
        {
          return this.x;
        }
        public int getY()
        {
          return this.y;
        }
        public int getVal()
        {
          return this.valeur;
        }
}
